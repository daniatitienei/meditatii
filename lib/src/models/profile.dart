class Profile {
  String uuid;
  String imgUrl;
  String tag;
  String firstName;
  String secondName;
  String email;
  String city;
  String description;
  String phoneNumber;
  String materie;
  String judet;
  int price;

  Profile({
    required this.uuid,
    required this.imgUrl,
    required this.tag,
    required this.firstName,
    required this.secondName,
    required this.email,
    required this.description,
    required this.city,
    required this.phoneNumber,
    required this.materie,
    required this.price,
    required this.judet,
  });
}
